package com.eka.ekaPricing.pojo;

public class Range {
	private String rangeLowerVal;
	private String rangeUpperVal;
	private String rangeOrder;
	private String rangeCompareString;
	private double basePD;
	private double incrementalValue;
	private double stepSize;
	private String rangeType;
	public String getRangeLowerVal() {
		return rangeLowerVal;
	}
	public void setRangeLowerVal(String rangeLowerVal) {
		this.rangeLowerVal = rangeLowerVal;
	}
	public String getRangeUpperVal() {
		return rangeUpperVal;
	}
	public void setRangeUpperVal(String rangeUpperVal) {
		this.rangeUpperVal = rangeUpperVal;
	}
	public String getRangeOrder() {
		return rangeOrder;
	}
	public void setRangeOrder(String rangeOrder) {
		this.rangeOrder = rangeOrder;
	}
	public String getRangeCompareString() {
		return rangeCompareString;
	}
	public void setRangeCompareString(String rangeCompareString) {
		this.rangeCompareString = rangeCompareString;
	}
	public double getBasePD() {
		return basePD;
	}
	public void setBasePD(double basePD) {
		this.basePD = basePD;
	}
	public double getIncrementalValue() {
		return incrementalValue;
	}
	public void setIncrementalValue(double incrementalValue) {
		this.incrementalValue = incrementalValue;
	}
	public double getStepSize() {
		return stepSize;
	}
	public void setStepSize(double stepSize) {
		this.stepSize = stepSize;
	}
	public String getRangeType() {
		return rangeType;
	}
	public void setRangeType(String rangeType) {
		this.rangeType = rangeType;
	}
	

}
