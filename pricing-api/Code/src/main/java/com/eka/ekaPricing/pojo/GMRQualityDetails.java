package com.eka.ekaPricing.pojo;

public class GMRQualityDetails {

	private String quality;
	private String curveName;
	private String curveQty;
	private String curveQtyUnitId;
	private String curveQtyUnitCheck;
	private double actualConversionFactorGMR;
	
	public String getQuality() {
		return quality;
	}
	public void setQuality(String quality) {
		this.quality = quality;
	}
	public String getCurveName() {
		return curveName;
	}
	public void setCurveName(String curveName) {
		this.curveName = curveName;
	}
	public String getCurveQty() {
		return curveQty;
	}
	public void setCurveQty(String curveQty) {
		this.curveQty = curveQty;
	}
	public double getActualConversionFactorGMR() {
		return actualConversionFactorGMR;
	}
	public void setActualConversionFactorGMR(double actualConversionFactorGMR) {
		this.actualConversionFactorGMR = actualConversionFactorGMR;
	}
	public String getCurveQtyUnitId() {
		return curveQtyUnitId;
	}
	public void setCurveQtyUnitId(String curveQtyUnitId) {
		this.curveQtyUnitId = curveQtyUnitId;
	}
	public String getCurveQtyUnitCheck() {
		return curveQtyUnitCheck;
	}
	public void setCurveQtyUnitCheck(String curveQtyUnitCheck) {
		this.curveQtyUnitCheck = curveQtyUnitCheck;
	}
	
}
