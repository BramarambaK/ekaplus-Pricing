package com.eka.ekaPricing.pojo;


public class PricingComponent {
	private String internalContractRefNo;
	private String internalContractItemRefNo;
	private String GMRRefNo;
	private String productComponentName;
	private double componentPercentage;
	private String internalGmrRefNo;
	public String getInternalContractRefNo() {
		return internalContractRefNo;
	}
	public void setInternalContractRefNo(String internalContractRefNo) {
		this.internalContractRefNo = internalContractRefNo;
	}
	public String getInternalContractItemRefNo() {
		return internalContractItemRefNo;
	}
	public void setInternalContractItemRefNo(String internalContractItemRefNo) {
		this.internalContractItemRefNo = internalContractItemRefNo;
	}
	public String getGMRRefNo() {
		return GMRRefNo;
	}
	public void setGMRRefNo(String gMRRefNo) {
		GMRRefNo = gMRRefNo;
	}
	public String getProductComponentName() {
		return productComponentName;
	}
	public void setProductComponentName(String productComponentName) {
		this.productComponentName = productComponentName;
	}
	public double getComponentPercentage() {
		return componentPercentage;
	}
	public void setComponentPercentage(double componentPercentage) {
		this.componentPercentage = componentPercentage;
	}
	public String getInternalGmrRefNo() {
		return internalGmrRefNo;
	}
	public void setInternalGmrRefNo(String internalGmrRefNo) {
		this.internalGmrRefNo = internalGmrRefNo;
	}
	
}
